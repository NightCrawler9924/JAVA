public class Whileloop 
{
    public static void main(String[] args) 
    {
        int i = 1;

        while (i<=5)
        {
            System.out.println("Hi" + i );
            
            int j= 3;
            while (j<=8)
            {
                System.out.println("Hello");
                j++;
            }
                
            i++;

        }


    }
    
}
